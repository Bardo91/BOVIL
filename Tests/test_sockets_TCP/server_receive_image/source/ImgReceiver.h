#ifndef _IMG_RECEIVER_IMG_RECEIVER_H_
#define _IMG_RECEIVER_IMG_RECEIVER_H_

//--------------------------------------------------------------------------------------------------------------------
struct ImageInfo{
	int mRows;
	int mCols;
	unsigned char * mData;

	//--------------------------------------------------------------------------------------------------------------------
	ImageInfo(): mRows(0), mCols(0), mData(nullptr){ }

	//--------------------------------------------------------------------------------------------------------------------
	// Move constructor;
	ImageInfo(ImageInfo&& _info) :	mRows(_info.mRows),
									mCols(_info.mCols),
									mData(_info.mData){
		_info.mData = nullptr;
	}
};


//---------------------------------------------------------------------------------------------------------------------
class ImgReceiver{
public:
	ImgReceiver();
	ImageInfo getImage();

private:

};


#endif	// IMG_RECEIVER_H_
